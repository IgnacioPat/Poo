package logica;

public class Sube {

	private String num;
	private int credito;
	
	public String getNum() {
		return num;
	}
	public void setNum(String num) {
		this.num = num;
	}
	public int getCredito() {
		return credito;
	}
	public void setCredito(int credito) {
		this.credito = credito;
	}
	public Sube(String num, int credito) {
		super();
		this.num = num;
		this.credito = credito;
	}
	
	
}
